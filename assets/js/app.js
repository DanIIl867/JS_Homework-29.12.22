let color = prompt('Traffic light color?', '');

switch (color) {
   case "red":
      if (color == "red") {
         alert('Stop!');
      }
      break;

   case "yellow":
      if (color == "yellow") {
         alert('Get ready!');
      }
      break;

   case "green":
      if (color == "green") {
         alert('You can go!');
      }
      break;
}